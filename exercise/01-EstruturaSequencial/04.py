"""
Faça um Programa que peça as 4 notas bimestrais e mostre a média.
"""
num1 = float(input("Digite o 1º nota: "))
num2 = float(input("Digite o 2º nota: "))
num3 = float(input("Digite o 3º nota: "))
num4 = float(input("Digite o 4º nota: "))
avg = (num1 + num2 + num3 + num4) / 4
print(f"A média das notas é: {avg}")