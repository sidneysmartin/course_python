# comparison operators
# Equality (==)
a = 10
b = 10
c = "10"
result1  = (a == b)
result2 = (a == c)
print(result1) # False
print(result2) # False

# Inequality (!=)
a = 10
b = 10
c = "10"
result1  = (a != b)
result2 = (a != c)
print(result1) # False
print(result2) # True

# Greater than (>), Less than (<)
a = 10
b = 5 
result1  = (a > b)
result2 = (a < b)
print(result1) # True
print(result2) # False

# Greater than or equal to (>=), Less than or equal to (<=)
a = 3
b = 7
result1  = (a >= b)
result2 = (a <= b)
print(result1) # False
print(result2) # True