# Addition

sum_result = 5 + 3
print(sum_result) #8

# Subtraction

difference = 5 - 3
print(difference) # 2

# Multiplication

product = 5 * 3
print(product) # 15

# Quotient

quotient = 15 / 3
print(quotient)

# Exponentiation

exponent = 3 ** 5
print(exponent) # 243

# Modulo

modulo = 15 % 3
print(modulo) # 0

# Floating-point numbers

float_number1 = 0.1 + 0.2
float_number2 = 3.14
print(format(float_number1, ".2f"))
print(type(float_number2))
