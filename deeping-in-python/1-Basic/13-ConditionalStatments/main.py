# if statement
x = 10
if x > 5:
  print(f"{x} is greater than 5")

# if-else statement
y = 8
if y < 5:
  print(f"{y} is less than 5")
else:
  print(f"{y} is greater than 5")

# if-elif-else statement
grade = 8.5
if grade < 7:
  print(f"{grade} is less than 7")
elif grade < 9:
  print(f"{grade} is between 7 and 9")
else:
  print(f"{grade} is greater than 9")