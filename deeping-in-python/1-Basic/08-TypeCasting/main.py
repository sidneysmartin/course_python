#Type Casting

# int()
float_num = 3.14
int_num = int(float_num)
print(type(int_num),int_num)

# float()
new_int = 5
new_float = float(new_int)
print(type(new_int), new_float) #5.0

# str()
new_num = "42"
new_str = str(new_num)
print(type(new_str) ,new_str)

# bool()
new_value = 0
bool_value = bool(new_value)
print(type(bool_value) ,bool_value)